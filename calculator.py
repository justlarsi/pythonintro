num1 = float(input("Enter first number"))
arithmetic = str(input("Enter arithmetic sigh"))
num2 = float(input("Enter second number"))
if arithmetic == '+':
    print(num1 + num2)
elif arithmetic == '-':
    print(num1 - num2)
elif arithmetic == '*':
    print(num1 * num2)
elif arithmetic == '/':
    print(num1 / num2)
